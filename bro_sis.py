#Петя и Катя – брат и сестра. Петя – студент, а Катя – школьница. Петя помогает Кате по математике. 
#Он задумывает два натуральных числа X и Y (X,Y≤1000), а Катя должна их отгадать.
# Для этого Петя делает две подсказки. Он называет сумму этих чисел S и их произведение P. Помогите Кате отгадать задуманные Петей числа.
#7, 10 -> 2, 5 (2 + 5 == 7; 2 * 5 == 10)

def guess_numbers():
    try:
        X = int(input('Ведите первое число от 1 го до 1000  '))
        Y = int(input("Введите второе число от 1 го до 1000  "))
        s =  int(input("Введите сумму чисел   "))
        p =  int(input('Ведите произведение чисел   '))
        if 1<= X <= 1000 and 1<=Y <= 1000 and X + Y == s and X * Y == p: 
            for X in range(1, min(s, p) + 1):
                Y = s - X
                if X * Y == p:
                    return f'Вы загадали числа {X}  и {Y}'
                
        else:
            return f'Введите другие числа'
    except ValueError:
        return 'Вы ввели не число'
    except KeyboardInterrupt:
        return "Ведены не все данные"

print(guess_numbers())